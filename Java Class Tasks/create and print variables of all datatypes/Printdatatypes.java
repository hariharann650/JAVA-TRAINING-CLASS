public class Printdatatypes{
	public static void main (String[]args){
		int integer = 34;
		float floating = 3.3f;
		long longer = 34343434343434L;
		char character = 'H';
		String sensentes = "Printing the all datatypes in java";
		double doubling = 3.343434;
		short shortnumbers = 3434;
		boolean trueorfalse = true;
		
		System.out.println("integer =0"+integer);
		System.out.println("float ="+floating);
		System.out.println("long ="+longer);
		System.out.println("char ="+character);
		System.out.println("String ="+sensentes);
		System.out.println("double ="+doubling);
		System.out.println("short ="+shortnumbers);
		System.out.println("boolean ="+trueorfalse);
	}
}